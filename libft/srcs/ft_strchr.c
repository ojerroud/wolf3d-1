#include "libft.h"

char		*ft_strchr(char const *s, int c)
{
	size_t			i;
	char			*str;

	str = (char *)s;
	if (c == 0)
		return (str + ft_strlen(s));
	i = 0;
	while (str[i])
	{
		if (str[i] == (char)c)
			return (&str[i]);
		i++;
	}
	return (NULL);
}
